    <div class="advices-alumnos-header">
        <h2 class="advices-alumnos-title">Recomendaciones para protegernos del sol</h2>
    </div>
    
    <div style="font-size: 1.1rem; line-height: 1.6; color: #374151; margin-bottom: 2rem; text-align: justify;">
        1. <strong>Revisa el semáforo todos los días</strong><br>
        Antes de salir al recreo o a realizar actividades al aire libre, revisa el semáforo para saber el nivel de radiación UV.
        <div style="margin-top: 0; padding-top: 0; line-height: 1.6;">
            <div style="display: flex; align-items: flex-start; margin-bottom: 0;">
                <span style="color: blue; margin-right: 5px;">&#9679;</span>
                <span style="flex: 1;"><strong>Bajo (1-2):</strong> Puedes estar al aire libre sin mucha preocupación, pero sigue usando protección.</span>
            </div>
            <div style="display: flex; align-items: flex-start; margin-bottom: 0;">
                <span style="color: yellow; margin-right: 5px;">&#9679;</span>
                <span style="flex: 1;"><strong>Moderado (3-5):</strong> Usa protector solar, gorra y gafas.</span>
            </div>
            <div style="display: flex; align-items: flex-start; margin-bottom: 0;">
                <span style="color: orange; margin-right: 5px;">&#9679;</span>
                <span style="flex: 1;"><strong>Alto (6-7):</strong> Evita estar mucho tiempo al sol directo.</span>
            </div>
            <div style="display: flex; align-items: flex-start; margin-bottom: 0;">
                <span style="color: red; margin-right: 5px;">&#9679;</span>
                <span style="flex: 1;"><strong>Muy alto (8-10):</strong> Usa protección completa y busca sombra.</span>
            </div>
            <div style="display: flex; align-items: flex-start; margin-bottom: 0;">
                <span style="color: purple; margin-right: 5px;">&#9679;</span>
                <span style="flex: 1;"><strong>Extremo (11+):</strong> ¡Evita salir si no es necesario!</span>
            </div>
        </div>
        2. <strong>Usa protector solar todos los días</strong><br>
        Aplica protector solar con un FPS de al menos 30, 20 minutos antes de salir y reaplica cada 2 horas.<br>
        3. <strong>Viste ropa adecuada</strong><br>
        Usa camisas de manga larga, pantalones frescos y de colores claros. Las telas de algodón son una buena opción.<br>
        4. <strong>Lleva gorra o sombrero de ala ancha</strong><br>
        ¡Protege tu cara, orejas y cuello. ¡Es un accesorio que salva piel!<br>
        5. <strong>Usa lentes de sol con protección UV</strong><br>
        No todos los lentes oscuros protegen, asegúrate de que tengan filtro UV certificado.<br>
        6. <strong>Busca sombra siempre que sea posible</strong><br>
        Especialmente entre las 10:00 am y las 4:00 pm, que es cuando el sol está más fuerte.<br>
        7. <strong>Hidrátate constantemente</strong><br>
        Lleva siempre tu pachón con agua. El calor y el sol pueden deshidratarte sin darte cuenta.
    </div>
