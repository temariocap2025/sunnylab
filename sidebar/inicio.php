        <h2 class="panel-title">¿Qué es SunnyLab?</h2>
    </div>
    
    <p style="font-size: 1.1rem; line-height: 1.6; color: #374151; margin-bottom: 2rem;text-align: justify;">
        SunnyLab es un sistema innovador que mide la radicación ultravioleta (UV) en el ambiente y la muestra de manera clara y sencilla. Este muestra colores que indican los niveles de radiación, desde segura hasta peligrosa, lo cual nos ayuda a tomar buenas decisiones sobre cuanto tiempo debemos estar bajo exposición solar.
        <br><br>SunnyLab fomenta el cuidado de la salud y prevención por daños por exposición al sol.
